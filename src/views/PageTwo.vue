Dziko żyjące koty (Felis silvestris) były naturalnymi drapieżnikami gryzoni. Dzięki swojej umiejętności polowania, koty zaczęły zbliżać się do ludzkich osad, gdzie łatwo mogły znaleźć pokarm. Z czasem ludzie zauważyli, że koty skutecznie kontrolują populację gryzoni, co pozwalało im na lepsze zabezpieczenie swoich zapasów. Korzyści płynące z tej współpracy zaczęły przyciągać koty bliżej do ludzi, a te z kolei zaczęły korzystać z resztek jedzenia pozostawionych przez ludzi.

